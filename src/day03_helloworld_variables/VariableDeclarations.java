package day03_helloworld_variables;

public class VariableDeclarations {

	public static void main(String[] args) {
		//declare single variable
		int apples;
		//declare multiple variables in single statement
		int oranges, bananas, mango;
		//declare variable and assign value in single statement
		int kiwi=14;
		System.out.println(kiwi);
		//declare and assign value to multiple variables in single statement
		int watermelon=11, grapes=100, cherries=12, apricots;//not usually used
		
	}

}
