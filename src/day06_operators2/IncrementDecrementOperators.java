package day06_operators2;

public class IncrementDecrementOperators {

	public static void main(String[] args) {
			int employees=34;
			System.out.println("Employees: "+employees);
			employees++;
			System.out.println("Employees: "+employees);
			employees++;
			System.out.println("Employees: "+employees);
			++employees;
			System.out.println("Employees: "+employees);
			employees--;
			System.out.println("Employees: "+employees);
			--employees;
			System.out.println("Employees: "+employees);
			
	}

}
