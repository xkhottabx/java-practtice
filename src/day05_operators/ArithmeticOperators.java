package day05_operators;

public class ArithmeticOperators {

	public static void main(String[] args) {
		// + -addition operator
		System.out.println(55+35);
		int chairs=140;
		System.out.println(chairs+7);
		int moreChairs=chairs+5;
		System.out.println(moreChairs);
		int tables=130;
		int total=tables+chairs;
		System.out.println(total);
		//The same with substraction, multiplication, division
		int linesOfCode=50;
		System.out.println(linesOfCode);
		linesOfCode=--linesOfCode;
		System.out.println("blalb "+linesOfCode);
		System.out.println(10.0/3);
	}

}
